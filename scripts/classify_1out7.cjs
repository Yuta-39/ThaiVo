// vocabulary.ts を読み込む
const tsContent = fs.readFileSync(tsPath, 'utf8');

// JSON 配列部分を抽出・パース
const match = tsContent.match(/export const vocabulary: VocabularyItem\[\] = (\[[\s\S]*\]);/);
if (!match) {
  console.error('❌ vocabulary 配列が見つかりませんでした');
  process.exit(1);
}

let items;
try {
  items = JSON.parse(match[1]);
} catch (e) {
  console.error('❌ JSON パースに失敗しました:', e.message);
  process.exit(1);
}

console.log(`✅ 読み込み完了: ${items.length} 語`);

let changedCount = 0;
let notFoundInVocab = [];

// words1out7 のそれぞれが既存 vocab に存在するかチェック
for (const w of words1out7) {
  const found = items.find(item => item.word === w);
  if (!found) {
    notFoundInVocab.push(w);
  }
}

// frequency を更新（リストにある単語のみ、他の単語は変更しない）
const updatedItems = items.map(item => {
  if (words1out7.has(item.word)) {
    if (item.frequency !== '7回中1回') {
      console.log(`  変更: "${item.word}" (${item.frequency} → 7回中1回)`);
      changedCount++;
    }
    return { ...item, frequency: '7回中1回' };
  }
  // リストにない単語は一切変更しない
  return item;
});

console.log(`\n✅ 変更件数: ${changedCount} 語`);

if (notFoundInVocab.length > 0) {
  console.log(`\n⚠️  vocabulary.ts に存在しなかった単語 (${notFoundInVocab.length} 語):`);
  notFoundInVocab.forEach(w => console.log(`  - ${w}`));
}

// 型定義部分を抽出して再構成
const typeDefMatch = tsContent.match(/^(export type VocabularyItem[\s\S]*?}\n)/);
if (!typeDefMatch) {
  console.error('❌ 型定義部分が見つかりませんでした');
  process.exit(1);
}

const typeDef = typeDefMatch[1];
const newTsContent = `${typeDef}\nexport const vocabulary: VocabularyItem[] = ${JSON.stringify(updatedItems, null, 2)};\n`;

// バックアップを作成してから書き込む
const backupPath = tsPath + '.bak';
fs.copyFileSync(tsPath, backupPath);
console.log(`\n📁 バックアップ作成: ${backupPath}`);

fs.writeFileSync(tsPath, newTsContent, 'utf8');
console.log(`✅ vocabulary.ts を更新しました`);

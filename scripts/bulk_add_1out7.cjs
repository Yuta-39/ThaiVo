// ─── ユーティリティ ──────────────────────────────────────────────────────────────

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

function callGemini(prompt) {
  return new Promise((resolve, reject) => {
    const body = JSON.stringify({
      contents: [{ parts: [{ text: prompt }] }],
      generationConfig: { temperature: 0.1, maxOutputTokens: 8192 }
    });

    const options = {
      hostname: 'generativelanguage.googleapis.com',
      path: `/v1beta/models/gemini-3.1-flash-lite:generateContent?key=${API_KEY}`,
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(body)
      }
    };

    const req = https.request(options, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try {
          const parsed = JSON.parse(data);
          if (parsed.candidates && parsed.candidates[0]) {
            resolve(parsed.candidates[0].content.parts[0].text);
          } else {
            reject(new Error('No candidates: ' + data));
          }
        } catch (e) {
          reject(new Error('Parse error: ' + data.substring(0, 200)));
        }
      });
    });

    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

function readVocabulary() {
  const content = fs.readFileSync(TS_PATH, 'utf8');
  const match = content.match(/export const vocabulary: VocabularyItem\[\] = (\[[\s\S]*\]);/);
  if (!match) throw new Error('vocabulary配列が見つかりません');
  return JSON.parse(match[1]);
}

function writeVocabulary(items) {
  const content = fs.readFileSync(TS_PATH, 'utf8');
  const typeDefMatch = content.match(/^(export type VocabularyItem[\s\S]*?}\n)/);
  if (!typeDefMatch) throw new Error('型定義が見つかりません');
  const newContent = `${typeDefMatch[1]}\nexport const vocabulary: VocabularyItem[] = ${JSON.stringify(items, null, 2)};\n`;
  fs.writeFileSync(TS_PATH, newContent, 'utf8');
}

function loadProgress() {
  if (fs.existsSync(PROGRESS_PATH)) {
    return JSON.parse(fs.readFileSync(PROGRESS_PATH, 'utf8'));
  }
  return { done: [], failed: [] };
}

function saveProgress(progress) {
  fs.writeFileSync(PROGRESS_PATH, JSON.stringify(progress, null, 2), 'utf8');
}

// ─── メイン処理 ─────────────────────────────────────────────────────────────────

async function main() {
  const items = readVocabulary();
  const existingWords = new Set(items.map(i => i.word));
  const progress = loadProgress();
  progress.failed = []; // 今回の実行用に失敗リストをリセット
  const doneSet = new Set(progress.done);

  // まだ処理されていない単語を抽出
  const wordsToProcess = ALL_WORDS.filter(w => !existingWords.has(w) && !doneSet.has(w));
  const alreadyExistsInVocab = ALL_WORDS.filter(w => existingWords.has(w));

  // 既存単語のfrequencyを7回中1回に更新
  let updatedExisting = 0;
  for (const item of items) {
    if (ALL_WORDS.includes(item.word) && item.frequency !== FREQUENCY) {
      item.frequency = FREQUENCY;
      updatedExisting++;
    }
  }
  if (updatedExisting > 0) {
    writeVocabulary(items);
    console.log(`✅ 既存単語のfrequencyを更新: ${updatedExisting}語`);
  }

  console.log(`📊 状況:`);
  console.log(`  指定単語数: ${ALL_WORDS.length}`);
  console.log(`  既にvocabularyに存在: ${alreadyExistsInVocab.length}`);
  console.log(`  処理済み(前回): ${doneSet.size}`);
  console.log(`  今回追加する単語: ${wordsToProcess.length}`);

  if (wordsToProcess.length === 0) {
    console.log('\n✅ すべての単語が処理済みです！');
    return;
  }

  // 20語ずつバッチ処理
  const BATCH_SIZE = 20;
  let maxId = Math.max(...items.map(i => i.id || 0), doneSet.size + items.length);

  for (let i = 0; i < wordsToProcess.length; i += BATCH_SIZE) {
    const batch = wordsToProcess.slice(i, i + BATCH_SIZE);
    const batchNum = Math.floor(i / BATCH_SIZE) + 1;
    const totalBatches = Math.ceil(wordsToProcess.length / BATCH_SIZE);

    console.log(`\n📦 バッチ ${batchNum}/${totalBatches}: ${batch.length}語を処理中...`);
    console.log(`  単語: ${batch.join(', ')}`);

    const prompt = `以下のタイ語単語リストについて、それぞれの単語の情報をJSONで返してください。

単語リスト:
${batch.map((w, idx) => `${idx + 1}. ${w}`).join('\n')}

各単語について以下の情報を生成してください：
- meaning: 日本語の意味（簡潔に1〜3語、例: 「法律」「押す」「値切る」）
- pronunciation: ローマ字発音（日本語話者向け、例: "got-mǎai", "gòt", "gòt-raa-khaa"）
- exampleTh: タイ語例文（その単語を使った自然な文、20〜40文字程度）
- exampleJa: 例文の日本語訳

必ず以下のJSON形式で返してください（他のテキストは一切含めないこと）：
[
  {
    "word": "単語",
    "meaning": "日本語の意味",
    "pronunciation": "ローマ字発音",
    "exampleTh": "タイ語例文",
    "exampleJa": "日本語訳"
  },
  ...
]`;

    let retries = 3;
    let success = false;

    while (retries > 0 && !success) {
      try {
        await sleep(5000); // API レート制限対策（15 RPM 制限のため5秒間隔）
        const response = await callGemini(prompt);

        // JSONを抽出
        const jsonMatch = response.match(/\[[\s\S]*\]/);
        if (!jsonMatch) {
          throw new Error('JSONが見つかりません: ' + response.substring(0, 200));
        }

        const batchData = JSON.parse(jsonMatch[0]);

        // vocabulary.ts に追加
        const currentItems = readVocabulary();
        let currentMaxId = Math.max(...currentItems.map(i => i.id || 0));

        for (const entry of batchData) {
          if (!entry.word || !entry.meaning) continue;
          // 既に存在する場合はスキップ
          if (currentItems.find(i => i.word === entry.word)) {
            console.log(`  ⚠️  スキップ（既存）: ${entry.word}`);
            continue;
          }
          currentMaxId++;
          currentItems.push({
            id: currentMaxId,
            word: entry.word,
            meaning: entry.meaning,
            pronunciation: entry.pronunciation || '',
            frequency: FREQUENCY,
            exampleTh: entry.exampleTh || '',
            exampleJa: entry.exampleJa || ''
          });
          console.log(`  ✅ 追加: ${entry.word} (${entry.meaning})`);
        }

        writeVocabulary(currentItems);

        // 進捗を保存
        for (const w of batch) {
          progress.done.push(w);
        }
        saveProgress(progress);

        console.log(`  ✅ バッチ ${batchNum} 完了`);
        success = true;

      } catch (e) {
        retries--;
        console.error(`  ❌ エラー (残り${retries}回): ${e.message}`);
        if (retries > 0) {
          console.log(`  ⏳ 60秒待機して再試行...`);
          await sleep(60000);
        } else {
          // 失敗した単語を記録
          for (const w of batch) {
            progress.failed.push(w);
          }
          saveProgress(progress);
          console.error(`  ❌ バッチ ${batchNum} 失敗。スキップして続行します。`);
        }
      }
    }
  }

  // 最終確認
  const finalItems = readVocabulary();
  const count1out7 = finalItems.filter(i => i.frequency === FREQUENCY).length;
  console.log(`\n🎉 完了！`);
  console.log(`  総語数: ${finalItems.length}`);
  console.log(`  7回中1回の語数: ${count1out7}`);

  if (progress.failed.length > 0) {
    console.log(`\n⚠️  失敗した単語 (${progress.failed.length}語):`);
    progress.failed.forEach(w => console.log(`  - ${w}`));
    console.log('\nもう一度スクリプトを実行すれば、失敗した単語を再処理します。');
  }
}

main().catch(err => {
  console.error('致命的エラー:', err);
  process.exit(1);
});
